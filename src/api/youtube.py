def auth():
    credentials = db.get_credentials()

    # 기존 인증 정보 사용
    if credentials is not None:
        credentials = Credentials.from_authorized_user_info(credentials)
        if not credentials.valid: #만약 만료된 경우
            request = Request()
            credentials.refresh(request)
            db.update_credentials(credentials.to_json())

    # 새로 인증은 안 함
    youtube = googleapiclient.discovery.build(
        api_service_name, api_version, credentials=credentials)
    return youtube

def re_auth():
    secret = db.get_secret()
    if secret is not None:
        flow = google_auth_oauthlib.flow.InstalledAppFlow.from_client_config(secret,scopes)
        credentials = flow.run_local_server(port=0)
        db.update_credentials(credentials.to_json())
        youtube = googleapiclient.discovery.build(
            api_service_name, api_version, credentials=credentials)
        return youtube
    else:
        return None

def create_broadcast(title,desc,youtube):
    # Disable OAuthlib's HTTPS verification when running locally.
    # *DO NOT* leave this option enabled in production.
    # os.environ["OAUTHLIB_INSECURE_TRANSPORT"] = "1"

    now = datetime.datetime.now(tz=datetime.timezone.utc)
    start_time = now.isoformat(timespec='milliseconds')

    request = youtube.liveBroadcasts().insert(
        part="snippet,contentDetails,status",
        body={
            "contentDetails": {
                "closedCaptionsType": "closedCaptionsDisabled",
                "enableDvr": True,
                "enableEmbed": True,
                "recordFromStart": True,
                "enableAutoStart": True,
                "enableAutoStop": True,
                "latencyPreference" : 'ultraLow'
            },
            "snippet": {
                "title": title,
                "description" : desc,
                "scheduledStartTime": start_time,
            },
            "status": {
                "privacyStatus": "public",
                "selfDeclaredMadeForKids" : False
            }
        }
    )
    response = request.execute()
    broadcast_id = response['id']

    youtube.liveBroadcasts().bind(
        part="id,contentDetails",
        id=broadcast_id,  
        streamId=db.get_setting('channel_stream_id')
    ).execute()

    db.update_temp('broadcast_id',broadcast_id)

def broadcast_live(youtube):
    broadcast_id = db.get_temp('broadcast_id')
    youtube.liveBroadcasts().transition(
        broadcastStatus="live",
        id=broadcast_id,
        part="status"
    ).execute()

def broadcast_comp(youtube):
    broadcast_id = db.get_temp('broadcast_id')
    youtube.liveBroadcasts().transition(
        broadcastStatus="complete",
        id=broadcast_id,
        part="status"
    ).execute()

