def get_stream_key(youtube):
    request = youtube.liveStreams().list(
        part="snippet,cdn,status",
        mine=True,
        maxResults=50
    )
    response = request.execute()

    for stream in response.get('items', []):
        if stream['snippet']['title'] == f'LiveKiosk Stream Key {get_mac()}':
            return stream['cdn']['ingestionInfo']['streamName'], stream['id']
    return create_stream_key(youtube)

def get_channel_name(youtube):
    request = youtube.channels().list(
        part="snippet",
        mine=True
    )
    response = request.execute()

    if response['items']:
        channel = response['items'][0]
        title = channel['snippet']['title']
        channel_id = channel['id']
        return f'{title} (ID : {channel_id})'
    else:
        return "채널 정보를 찾을 수 없습니다."

